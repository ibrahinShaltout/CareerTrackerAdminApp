package com.example.ucgpadmin.academy;


public class CourseDataClass {

    String course_Link;
    String course_Topic;
    String course_hours;
    String course_name;
    String course_overview;
    String course_price;
    String course_Certification;


    public String getCourse_Certification() {
        return course_Certification;
    }

    public void setCourse_Certification(String course_Certification) {
        this.course_Certification = course_Certification;
    }

    public String getCourse_Link() {
        return course_Link;
    }

    public void setCourse_Link(String course_Link) {
        this.course_Link = course_Link;
    }

    public String getCourse_Topic() {
        return course_Topic;
    }

    public void setCourse_Topic(String course_Topic) {
        this.course_Topic = course_Topic;
    }

    public String getCourse_hours() {
        return course_hours;
    }

    public void setCourse_hours(String course_hours) {
        this.course_hours = course_hours;
    }

    public String getCourse_name() {
        return course_name;
    }

    public void setCourse_name(String course_name) {
        this.course_name = course_name;
    }

    public String getCourse_overview() {
        return course_overview;
    }

    public void setCourse_overview(String course_overview) {
        this.course_overview = course_overview;
    }

    public String getCourse_price() {
        return course_price;
    }

    public void setCourse_price(String course_price) {
        this.course_price = course_price;
    }
}
